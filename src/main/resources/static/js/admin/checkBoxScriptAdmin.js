function updateValue1() {
    const checkbox = document.getElementById('checkbox1');
    const id = 1; // Идентификатор записи, которую нужно обновить
    const isChecked = checkbox.checked; // Значение чекбокса (true или false)

    fetch('/admin/admin1?id=' + id + '&isChecked=' + isChecked, {

        method: 'POST'
    })
}

